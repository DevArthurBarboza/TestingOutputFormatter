public class Integer {
    public static void main(String[] args) {

        // Testing output formatter with integer value
        System.out.printf("%d\n", 26);
        System.out.printf("%d\n", -26);
        System.out.printf("%d\n", +26);
        System.out.printf("%o\n", 26);
        System.out.printf("%x\n", 26);
        System.out.printf("%X\n", 26);


    }
}
