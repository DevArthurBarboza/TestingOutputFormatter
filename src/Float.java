public class Float {
    public static void main(String[] args) {

        System.out.printf("%.3e\n",150.450000001);
        System.out.printf("%e\n",150.450000001);
        System.out.printf("%.3f\n",150.45000);
        System.out.printf("%.2g\n",150.45000);
        System.out.printf("%e\n",-0.000015045000);
        System.out.printf("%f\n",-0.000015045000);
        System.out.printf("%g\n",-0.000015045000);



    }
}
